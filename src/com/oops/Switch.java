package com.oops;

public class Switch {
	
public static void main(String[] args) {
	
	// switch ------ if the condition matched the code it willbe executed
	
	char ch =0;
	
	switch (ch) {
	
	case 'a':
		
		System.out.println(" today is egg day");
		
		break;
		
	case 'b':
		
		System.out.println("today is chicken day");
		
		break;
		
	case 'c':
		
		System.out.println("today is soya day");
		
		break;
		
		
	case 'd':
		
		System.out.println("today is peanut day");
		
break;		
		default:
			
			System.out.println("invalid input");
	}
}

}
